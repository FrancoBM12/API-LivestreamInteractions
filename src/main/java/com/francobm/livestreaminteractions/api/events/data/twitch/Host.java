public class Host {
    private String name;
    private String viewers;
    private String type;
    private String _id;

    public String getName() {
        return name;
    }

    public String getViewers() {
        return viewers;
    }

    public String getType() {
        return type;
    }

    public String get_id() {
        return _id;
    }
}
